#常見的佔位符有三種：

#1.%d　　整數佔位符
print('我考了%d分' % 20) #若%後接小數,還是指印出整數部分
print("我考了%d分，進步了%d分" % (50,10)) #用(x,y)來連接多個值
#2.%f　　浮點數佔位符
print("我考了%f" % 66.666)
print('我考了%f' % 66)  #印出我考了66.000000
print("我考了%f，進步了%.2f" % (66,12.369)) #%.xf 則是精確至小數點後x位
#%s佔位字串，應用最多
print("%s is the goat" %"michael Jordan")
#注意: print(%d+1 %x) 是錯誤的,正確為print("%d" %(x+1))